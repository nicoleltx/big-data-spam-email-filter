stopword=[]
for word in stopwords:
    word=str(' ')+word+str(' ')
    stopword.append(word)
#print(stopword)
#print('hello')

def rmstop(text):
    text_l=text.split(' ')
    clean_t=''
    for word in text_l:
        if not word in stopwords:
            clean_t=clean_t+str(' ')
            clean_t=clean_t+word
    return clean_t


sc = SparkContext()

devstatus = sc.textFile("/lemm/part1")

cleanstatus = devstatus. \
    map(lambda line: line.lower()). \
    map(lambda points: rmstop(points)). \
    map(lambda t4: t4.replace('$','dollar')). \
    map(lambda fields: fields.translate(str.maketrans('','',string.punctuation)))
    
   

# Save to a CSV file as a comma-delimited string (trim parenthesis from tuple toString)
cleanstatus. \
    saveAsTextFile("/lemm/clean")

